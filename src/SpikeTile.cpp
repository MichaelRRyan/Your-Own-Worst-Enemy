#include "SpikeTile.h"

///////////////////////////////////////////////////////////////////
SpikeTile::SpikeTile(int const t_textureNum) :
	m_textureNum{ t_textureNum }
{
}

///////////////////////////////////////////////////////////////////
sf::IntRect const SpikeTile::getTextureRect() const
{
	switch (m_textureNum)
	{
	case 1:
		return { 48, 0, 16, 16 };
	default:
		return { 48, 16, 16, 16 };
	}
}

///////////////////////////////////////////////////////////////////
bool const SpikeTile::isSolid() const
{
	return false;
}

///////////////////////////////////////////////////////////////////
bool const SpikeTile::isVisible() const
{
	return true;
}

///////////////////////////////////////////////////////////////////
bool const SpikeTile::isDeadly() const
{
	return true;
}

